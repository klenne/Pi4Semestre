package model.bean;

public class Requisitos {
	private int codRequisito, codProduto;
	private String interesseEm;
	public int getCodRequisito() {
		return codRequisito;
	}
	public void setCodRequisito(int codRequisito) {
		this.codRequisito = codRequisito;
	}
	public int getCodProduto() {
		return codProduto;
	}
	public void setCodProduto(int codProduto) {
		this.codProduto = codProduto;
	}
	public String getInteresseEm() {
		return interesseEm;
	}
	public void setInteresseEm(String interesseEm) {
		this.interesseEm = interesseEm;
	}
	  

}
